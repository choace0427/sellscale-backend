from model_import import Prospect
from app import db
from src.prospecting.models import ProspectStatus
from tqdm import tqdm


def find_prospects_from_data(data):
    for d in tqdm(data):
        if len(d["linkedInUrls"]) > 1:
            continue

        linkedin_url = d["linkedInUrls"][0]
        linkedin_url = linkedin_url[0 : len(linkedin_url) - 1].replace(
            "https://www.", ""
        )

        last_message_from_prospect = linkedin_url in d["lastMessageFromUrl"]

        p = Prospect.query.filter(Prospect.linkedin_url.ilike(linkedin_url)).first()
        if (
            p
            and p.status == ProspectStatus.SENT_OUTREACH
            and last_message_from_prospect == True
        ):
            full_name = p.full_name
            linkedin_url = p.linkedin_url
            status = p.status

            print("{}\t\t{}\t\t{}".format(full_name, linkedin_url, status))
            print(
                "Last message from prospect: {}".format(str(last_message_from_prospect))
            )
            print()


